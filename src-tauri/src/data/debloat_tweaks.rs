use crate::models::debloat::{TweakCategory, TweakTier, TweakType};

pub struct TweakDefinition {
    pub id: &'static str,
    pub name: &'static str,
    pub description: &'static str,
    pub tier: TweakTier,
    pub category: TweakCategory,
    pub tweak_type: TweakType,
    pub is_reversible: bool,
    pub warning: Option<&'static str>,
    pub registry_entries: Vec<RegistryEntry>,
    pub appx_name: Option<&'static str>,
    pub service_name: Option<&'static str>,
}

pub struct RegistryEntry {
    pub hive: &'static str,
    pub path: &'static str,
    pub name: &'static str,
    pub value: u32,
}

fn reg(hive: &'static str, path: &'static str, name: &'static str, value: u32) -> RegistryEntry {
    RegistryEntry {
        hive,
        path,
        name,
        value,
    }
}

